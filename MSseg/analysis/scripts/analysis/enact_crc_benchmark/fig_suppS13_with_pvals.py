# ── Bootstrap helpers ──────────────────────────────────────────────────────────

def load_covered(slug: str) -> tuple[np.ndarray, np.ndarray]:
    """Load covered-subset cells (pred_label in CLASSES) for a method."""
    df = pd.read_csv(RESULTS_DIR / f"gt_matched_{slug}.csv")
    df = df[df["pred_label"].isin(CLASSES)].reset_index(drop=True)
    return df["gt_label"].values, df["pred_label"].values


def bootstrap_wf1_ci(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    n: int = N_BOOTSTRAP,
    seed: int = RNG_SEED,
) -> tuple[float, float, float]:
    """Return (point, lo_95, hi_95) for weighted F1."""
    rng = np.random.default_rng(seed)
    size = len(y_true)
    scores = []
    for _ in range(n):
        idx = rng.integers(0, size, size=size)
        scores.append(
            f1_score(y_true[idx], y_pred[idx],
                     average="weighted", labels=CLASSES, zero_division=0)
        )
    point = f1_score(y_true, y_pred, average="weighted",
                     labels=CLASSES, zero_division=0)
    return point, float(np.percentile(scores, 2.5)), float(np.percentile(scores, 97.5))


def bootstrap_per_class_ci(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    n: int = N_BOOTSTRAP,
    seed: int = RNG_SEED,
) -> dict[str, tuple[float, float, float]]:
    """Return {class: (point, lo_95, hi_95)} for each class F1."""
    rng = np.random.default_rng(seed)
    size = len(y_true)
    cls_scores: dict[str, list[float]] = {c: [] for c in CLASSES}
    for _ in range(n):
        idx = rng.integers(0, size, size=size)
        per = f1_score(y_true[idx], y_pred[idx], average=None,
                       labels=CLASSES, zero_division=0)
        for c, v in zip(CLASSES, per):
            cls_scores[c].append(float(v))
    per_point = f1_score(y_true, y_pred, average=None,
                         labels=CLASSES, zero_division=0)
    return {
        c: (float(per_point[i]),
            float(np.percentile(cls_scores[c], 2.5)),
            float(np.percentile(cls_scores[c], 97.5)))
        for i, c in enumerate(CLASSES)
    }


def bootstrap_permutation_p(
    y_true_a: np.ndarray, y_pred_a: np.ndarray,
    y_true_b: np.ndarray, y_pred_b: np.ndarray,
    n: int = N_BOOTSTRAP, seed: int = RNG_SEED,
) -> float:
    """Two-sided p-value: P(|diff_null| >= |obs_diff|) via permutation bootstrap."""
    rng = np.random.default_rng(seed)
    f1_a = f1_score(y_true_a, y_pred_a, average="weighted",
                    labels=CLASSES, zero_division=0)
    f1_b = f1_score(y_true_b, y_pred_b, average="weighted",
                    labels=CLASSES, zero_division=0)
    obs_diff = abs(f1_b - f1_a)

    all_true = np.concatenate([y_true_a, y_true_b])
    all_pred = np.concatenate([y_pred_a, y_pred_b])
    n_a, n_b = len(y_true_a), len(y_true_b)
    null_diffs = []
    for _ in range(n):
        perm = rng.permutation(len(all_pred))
        ia, ib = perm[:n_a], perm[n_a:n_a + n_b]
        fa = f1_score(all_true[ia], all_pred[ia], average="weighted",
                      labels=CLASSES, zero_division=0)
        fb = f1_score(all_true[ib], all_pred[ib], average="weighted",
                      labels=CLASSES, zero_division=0)
        null_diffs.append(abs(fb - fa))
    return float(np.mean(np.array(null_diffs) >= obs_diff))


def pvalue_stars(p: float) -> str:
    if p < 0.001: return "***"
    if p < 0.01:  return "**"
    if p < 0.05:  return "*"
    return "ns"


# ── Load per-cell data and compute bootstrap stats ────────────────────────────

print("Loading per-cell data and computing bootstrap CIs (n=5,000)…")
wf1_stats: dict[str, tuple[float, float, float]] = {}
class_stats: dict[str, dict[str, tuple[float, float, float]]] = {}
cell_data: dict[str, tuple[np.ndarray, np.ndarray]] = {}

for m in methods_order:
    slug = METHOD_SLUGS[m]
    y_true, y_pred = load_covered(slug)
    cell_data[m] = (y_true, y_pred)
    point, lo, hi = bootstrap_wf1_ci(y_true, y_pred)
    wf1_stats[m] = (point, lo, hi)
    class_stats[m] = bootstrap_per_class_ci(y_true, y_pred)
    print(f"  {m:10s}  n={len(y_true):,}  wF1={point:.3f} [{lo:.3f}, {hi:.3f}]")

# Pairwise permutation test vs MCseg
print("\nPairwise bootstrap permutation tests vs MCseg…")
mcseg_true, mcseg_pred = cell_data["MCseg"]
pvals: dict[str, float] = {}
for m in methods_order:
    if m == "MCseg":
        continue
    y_true, y_pred = cell_data[m]
    p = bootstrap_permutation_p(mcseg_true, mcseg_pred, y_true, y_pred)
    pvals[m] = p
    print(f"  MCseg vs {m:10s}  p={p:.4f}  {pvalue_stars(p)}")

